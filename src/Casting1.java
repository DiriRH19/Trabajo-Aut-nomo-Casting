
public class Casting1 {

	public static void main(String[] args) {
		
		double numeroDecimal = 10.5;
		int numeroEntero = (int) numeroDecimal;
		
		System.out.println("El número entero queda de la siguiente manera: " + numeroEntero);
		
		/* 
		 * Con este casting convertimos un número decimal 
		 * en un entero, y el 10.5 se reemplaza solo por un 10
		 */
		
	}

}
